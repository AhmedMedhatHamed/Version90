class Student {
  String name;
  String code;
  String religion;
  String gender;
  String nationality;
  String birthDate;
  String birthPlace;
  String nationalId;
  String address;
  String phone;
  String mobile;
  String email;

  Student({
    required this.name,
    required this.code,
    required this.religion,
    required this.gender,
    required this.nationality,
    required this.birthDate,
    required this.birthPlace,
    required this.nationalId,
    required this.address,
    required this.phone,
    required this.mobile,
    required this.email,
  });
}
